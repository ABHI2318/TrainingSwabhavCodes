package com.aurionpro.first;

public class swapNumber {
	public static void main(String[] args) {
	
		int value1=10;
		int value2=15;
		
		System.out.println("before swap first number:"+value1);
		System.out.println("before swap second number:"+value2);
		
		int tempor=value1;
		value1=value2;
		value2=tempor;
		System.out.println("After swap first number:"+value1);
		System.out.println("After swap second number:"+value2);
		
	
		
		

	}

}

